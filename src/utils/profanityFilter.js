import Filter from 'badwords-ko';

const filter = new Filter();

// 라이브러리가 잡지 못하는 추가 단어
const extraWords = [
  // 자음 축약형
  'ㅅㅂ', 'ㅆㅂ', 'ㅂㅅ', 'ㅈㄴ', 'ㅈㄹ', 'ㅅㄲ', 'ㄱㅅㄲ', 'ㅁㅊ', 'ㅍㅌㅊ',
  // 씨발 계열
  '씨발', '씨팔', '시발', '시팔', '씨밸', '시밸', '씨봐', '씨빠',
  // 새끼 계열
  '새끼', '세끼', '쉐끼', '쉑', '새기',
  // 개 접두 욕설
  '개새끼', '개세끼', '개쉐끼', '개같은', '개같', '개소리', '개판', '개쓰레기',
  '개념없', '개년', '개놈', '개새', '개돼지',
  // 병신 계열
  '병신', '봉신', '벙신', '찐따', '빡대가리', '뇌절',
  // 미친 계열
  '미친', '미친놈', '미친년', '미친새끼', '미쳤',
  // 존나/졸라 계열
  '존나', '존내', '졸라', '졸나',
  // 지랄 계열
  '지랄', '지럴', '지랄맞',
  // 기타
  '닥쳐', '꺼져', '뒤져', '죽어', '엿같', '엿먹',
  '썅', '쌍년', '쌍놈',
  '좆', '좃', '씹',
];

filter.addWords(...extraWords);

// 라이브러리가 단어 경계를 인식하지 못하는 패턴 (자음 축약형, 개~ 등) 별도 정규식 처리
const EXTRA_REGEX = new RegExp(
  [
    'ㅅㅂ', 'ㅆㅂ', 'ㅂㅅ', 'ㅈㄴ', 'ㅈㄹ', 'ㅅㄲ', 'ㄱㅅㄲ', 'ㅁㅊ', 'ㅍㅌㅊ',
    '개새끼', '개세끼', '개쉐끼', '개같', '개소리', '개판', '개쓰레기', '개년', '개놈', '개새', '개돼지',
    '씨발', '씨팔', '시발', '시팔', '씨밸', '시밸',
    '새끼', '세끼', '쉐끼',
    '병신', '봉신', '찐따', '빡대가리',
    '미친놈', '미친년', '미친새끼', '미쳤',
    '존나', '존내', '졸라', '졸나',
    '지랄', '지럴',
    '닥쳐', '꺼져', '뒤져',
    '썅', '쌍년', '쌍놈',
    '좆', '좃', '씹',
  ].map(w => w.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')).join('|'),
  'gi'
);

export function cleanProfanity(text) {
  if (!text) return text;
  // 1차: 라이브러리 필터
  let result = filter.clean(text);
  // 2차: 라이브러리가 놓친 패턴 정규식 처리
  result = result.replace(EXTRA_REGEX, (match) => '*'.repeat(match.length));
  return result;
}